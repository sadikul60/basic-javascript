// Type of data tree.
// 1. Numeric or Number type ;
// 2. String type;
// 3. Boolean type;

// 1. Number type data;
var num = 44;
console.log(num);
// Output: 44
console.log(typeof num);
// Output: number


// 2. String type data;
var name = 'Asadujjaman';
console.log(name);
// Output: Asadujjaman
console.log(typeof name);
// Output: string


// 3. boolean type data;
var areYouSure = true;
console.log(areYouSure);
// Output: true
console.log(typeof areYouSure);
// Output: boolean
